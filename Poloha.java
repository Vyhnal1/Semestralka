
public class Poloha {
    private final int x;
    private final int y;
    
    public Poloha(int x, int y) {
        // initialise instance variables
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return this.x;
    }
    
    public int getY() {
        return this.y;
    }
    
}
